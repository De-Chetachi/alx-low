#include <stdio.h>
/**
* main - entry point.
* Return: Always 0
*/

int     main(void)
{
        char    cheta;

	char	CHETA;

        for     (cheta  =       'a';    cheta   <=      'z';    cheta++)
        {
                putchar(cheta);
        }

	for     (CHETA  =       'A';    CHETA   <=      'Z';    CHETA++)
	{
		putchar(CHETA);
	}

        putchar('\n');

        return (0);
}
